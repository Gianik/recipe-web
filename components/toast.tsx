import { toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';
export const toastError = (message: String) => { // Show a toast for error message
    toast.error(message, {
        position: "top-center",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
      });
}
